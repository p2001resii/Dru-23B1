package Tasks3rdmay3;

public class SwitchVowelExample 
{
	public static void main(String[] args) 
	{
	  char Ch='A';
	  switch(Ch)
	  {

	  case 'a':
	  case 'e':
	  case 'i':
	  case 'o':
	  case 'u':
	  case 'A':
	  case 'E':
	  case 'I':
	  case 'O':
	  case 'U':
		  System.out.println("vowel");
		  break;
	  default:
		  System.out.println("consonant");
	  }
	}
}
		  
			  