package Tasks3rdmay;

public class OperatorsTask 
{
	public static void main(String[] args) 
	{
		int a=9;
		a++;    //a=10
		System.out.println(a);         //a=10
		a--; //a=9
		System.out.println(a);        // a=9;    
		a++;  //a=10;
		System.out.println(a);         //a=10
		System.out.println(a++);       //a=10 
				//a=10+1  a=11
		System.out.println(a--);         //a=11
		System.out.println(--a);  	 	 //a=9
		                 //a=10-9  a=9
		
		System.out.println(++a);      //a=10
		
	}

}
